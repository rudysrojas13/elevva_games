function normalize(title) {
  return title
    .toLowerCase()
    .replace(/[‚ĄĘ¬ģ¬©]/g, '')
    .replace(/\s+/g, ' ')
    .replace(/[^a-z0-9]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function cleanTitle(name) {
  let title = name;
  title = title.replace(/\(([^)]+)\)/g, '');
  title = title.split(' (PS')[0].split(' | Alquiler')[0].split(' | P2')[0];
  title = title.replace(/[ūüí≥‚≠źūüĒ•‚ö°ūüĒįūüüĘ]/g, '');
  title = title.replace(/(Gold|Deluxe|Standard|Ultimate|Complete|Definitive|Legacy|GOTY|Game of the Year|Special|Legendary|Enhanced|Remastered|Remake|Eternal)\s+Edition/gi, '');
  title = title.replace(/\b(Gold|Deluxe|Ultimate|Complete|Definitive|Remastered|Remake|Eternal|Collection)\b/gi, '');
  title = title.replace(/Alquiler\s+\d+\s+d√≠as/gi, '');
  title = title.replace(/Alquiler/gi, '');
  title = title.replace(/Rent\s+\d+\s+days/gi, '');
  title = title.replace(/Rent/gi, '');
  return title.trim().toLowerCase().replace(/\s+/g, ' ');
}

async function main() {
  const catalogPath = 'C:\\Users\\pc\\Downloads\\seller_432476_all_products.json';
  const catalogData = JSON.parse(fs.readFileSync(catalogPath, 'utf8'));
  const sellerProducts = catalogData.product || [];

  // Group matched listings by game and category, then keep only the cheapest one!
  const cheapestMatches = {};

  for (const game of userGamesList) {
    const normGame = normalize(game);
    for (const sp of sellerProducts) {
      const normSp = normalize(sp.name);
      
      if (normSp.includes(normGame) || normGame.includes(normSp)) {
        const nameLower = sp.name.toLowerCase();
        const price = parseFloat(sp.price) || 0;
        
        // Check console category
        if (nameLower.includes('ps5')) {
          const key = `${cleanTitle(game)}_PS5`;
          if (!cheapestMatches[key] || price < cheapestMatches[key].price) {
            cheapestMatches[key] = { game, product: sp, price, category: 'PS5' };
          }
        }
        if (nameLower.includes('ps4')) {
          const key = `${cleanTitle(game)}_PS4`;
          if (!cheapestMatches[key] || price < cheapestMatches[key].price) {
            cheapestMatches[key] = { game, product: sp, price, category: 'PS4' };
          }
        }
      }
    }
  }

  const selectedMatches = Object.values(cheapestMatches);
  console.log(`Deduplicated: Found ${selectedMatches.length} unique cheapest game listings to import.`);

  // Check how many of these selected matches are already in the DB
  const targets = selectedMatches.map(m => `${m.product.id}-${m.category}`);
  const existingProducts = await prisma.product.findMany({
    where: {
      id: { in: targets }
    },
    select: { id: true }
  });

  const existingSet = new Set(existingProducts.map(p => p.id));
  
  let newCount = 0;
  let inDbCount = 0;
  const listToImport = [];

  for (const m of selectedMatches) {
    const key = `${m.product.id}-${m.category}`;
    if (existingSet.has(key)) {
      inDbCount++;
    } else {
      newCount++;
      listToImport.push(m);
    }
  }

  console.log(`Deduplicated matching details:`);
  console.log(`- Already in Database: ${inDbCount}`);
  console.log(`- New (to import): ${newCount}`);
  
  // Save deduplicated import list to a JSON file so the importer script can read it directly!
  fs.writeFileSync('C:\\Users\\pc\\Downloads\\cheapest_import_list.json', JSON.stringify(listToImport, null, 2));
  console.log('Saved cheapest import list to C:\\Users\\pc\\Downloads\\cheapest_import_list.json');
}

